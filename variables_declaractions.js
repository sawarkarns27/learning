// let, const, var

//#1. var: var variables can be re-declared and updated And access globally

	var varFirstName = "var: Tushar";
	console.log(varFirstName);


//#2. const: const cannot be updated or re-declared And not access outside the function
	const constFirstName = "const: Tushar";
	console.log(constFirstName);


//#3. let: let can be updated but not re-declared And not access outside the function
	const letFirstName = "let: Tushar";
	console.log(letFirstName);


